package br.com.sg.mechanical.error;


public class MessageDetails {
    private String title;
    private int status;
    private String detail;
    private long timestamp;
    private String developerMessage;
    private String field;
    private String fieldMessage;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }

    public String getDeveloperMessage() {
        return developerMessage;
    }

    public void setDeveloperMessage(String developerMessage) {
        this.developerMessage = developerMessage;
    }


    public static final class Builder {
        private String title;
        private int status;
        private String detail;
        private long timestamp;
        private String developerMessage;

        private Builder() {
        }

        public static Builder aMessageDetails() {
            return new Builder();
        }

        public Builder title(String title) {
            this.title = title;
            return this;
        }

        public Builder status(int status) {
            this.status = status;
            return this;
        }

        public Builder detail(String detail) {
            this.detail = detail;
            return this;
        }

        public Builder timestamp(long timestamp) {
            this.timestamp = timestamp;
            return this;
        }

        public Builder developerMessage(String developerMessage) {
            this.developerMessage = developerMessage;
            return this;
        }

        public MessageDetails build() {
            MessageDetails messageDetails = new MessageDetails();
            messageDetails.setTitle(title);
            messageDetails.setStatus(status);
            messageDetails.setDetail(detail);
            messageDetails.setTimestamp(timestamp);
            messageDetails.setDeveloperMessage(developerMessage);
            return messageDetails;
        }
    }
}
